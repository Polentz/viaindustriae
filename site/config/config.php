                    if ($newPage->paymentComplete()->isTrue()) {
                        kirby()->impersonate('kirby');
                        $newPage = $newPage->update([
                            'paidDate' => date('c'),
                        ]);
                    } else {
                        $newPage->update([
                            'paidDate' => '',
                        ]);
                    }
                }
